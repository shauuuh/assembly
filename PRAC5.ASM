;SUMAR DOS VECTORES DE NUMEROS ENTEROS DE 16 BITS.

DATOS SEGMENT PARA 'DATA'
	N DW 3
	V1 DW 4241H, 4443H, 4645H
	V2 DW 0000H, 0000H, 0000H
	V3 DW 0000H, 0000H, 0000H,'$'
DATOS ENDS

PILA SEGMENT PARA STACK 'STACK'
	DW      100     DUP(0)
PILA ENDS

CODIGO SEGMENT PARA 'CODE'
	ASSUME  CS:CODIGO, DS:DATOS, SS:PILA, ES:NOTHING
	
	PRINCIPAL PROC FAR
	
		PUSH DS
		XOR AX,AX
		PUSH AX
		MOV AX, DATOS
		MOV DS, AX
		MOV ES, AX
			
INICIO: MOV CX,N
		MOV SI,0
		
		;SUMAMOS ELEMENTO A ELEMENTO DEL VECTOR
CICLO:  MOV AX,V1[SI]
		ADD V3[SI],AX
		MOV AX,V2[SI]
		ADD V3[SI],AX
		ADD SI,2
		LOOP CICLO
		
		;MOSTRAMOS LA SUMA DEL VECTOR RESULTANTE
MOSTRAR:LEA DX,V3
		MOV AH,09
		INT 21H
		RET
		
	PRINCIPAL ENDP

CODIGO ENDS
    END PRINCIPAL
